"""Fixed evaluation harness for the `organsmnist` model-training task.

Contract
--------
You train an image classifier (any architecture, any recipe) and save it as a
self-contained **TorchScript** artifact ``model.pt``. This harness loads it,
runs it over a fixed split, and scores it with **accuracy + AUC** computed the
same way the official MedMNIST leaderboard reports them (top-1 accuracy; AUC =
macro one-vs-rest ``roc_auc_score`` over the 11 classes).

There are three fixed splits, read straight from the vendored
``data/organsmnist.npz`` (no network, no ``medmnist`` package, no download):

- ``train`` — what your training script learns from (a frozen 1/4 subsample, for
  speed; see ``TRAIN_SUBSAMPLE``). ``test`` is likewise a frozen 1/4 subsample
  (``TEST_SUBSAMPLE``); ``val`` is always scored in full.
- ``val``   — iterate here. ``evaluate(model)`` returns a *rich* per-item log
  (prediction vs. gold, an 11x11 confusion matrix, per-class recall/precision,
  and the list of misclassified items) so you can see exactly what to fix.
- ``test``  — the bar. Scored via ``submit.py`` (re-runnable). The test result is
  *aggregate-only*: no per-item predictions or gold labels are returned, so you
  see the test number but not which individual held-out items you missed.

    from eval import evaluate
    result = evaluate("model.pt")          # val split by default
    print(result["auc"], result["accuracy"])
    print(result["per_class"])             # per-class detail (val/train only)

Or from the command line (val iteration):

    python eval.py --model model.pt
    python eval.py --model model.pt --split train   # check for overfitting

For the held-out test number (re-runnable), use the separate submitter:

    python submit.py --model model.pt

DO NOT EDIT this file when iterating — only your *model* changes. Keeping the
harness fixed (the data splits, the preprocessing, the answer-extraction, and the
metric) is what makes scores comparable across training runs. Your training
script imports the data/preprocessing helpers from here so that train and eval
see byte-identical inputs.

The model artifact contract (read carefully)
---------------------------------------------
``model.pt`` must be a TorchScript module (``torch.jit.save``) such that:

  * input  : a float32 tensor of shape ``[N, 1, 28, 28]``, already normalized
             with mean=0.5, std=0.5 (the canonical MedMNIST transform — exactly
             what ``load_dataset()`` below produces from the raw uint8 images).
  * output : raw class logits of shape ``[N, 11]`` (this harness applies softmax).

Use ``load_dataset`` from this module in your training script so the model is
trained on precisely the tensors it will be scored on.

Dependencies: ``torch``, ``numpy``, ``scikit-learn`` (see ``requirements.txt`` —
``pip install -r requirements.txt``). Run ``python prepare_data.py`` once to
verify the vendored splits.
"""

from __future__ import annotations

import argparse
import json
import os
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

import numpy as np
import torch

# --- Frozen task config --------------------------------------------------------
# These pin the dataset and its shape. To stamp out a sibling task for another
# MedMNIST dataset, this block + the labels are the only things that change.
DATASET = "organsmnist"
TASK_TYPE = "multi-class"       # matches the published MedMNIST task type
N_CHANNELS = 1
N_CLASSES = 11
IMG_SIZE = 28
LABELS = {
    0: "bladder", 1: "femur-left", 2: "femur-right", 3: "heart",
    4: "kidney-left", 5: "kidney-right", 6: "liver", 7: "lung-left",
    8: "lung-right", 9: "pancreas", 10: "spleen",
}

# Canonical MedMNIST normalization (used by the official example code). Frozen so
# that "what the model sees at eval" is fixed; train with the same transform.
NORM_MEAN = 0.5
NORM_STD = 0.5

TASK_DIR = Path(__file__).resolve().parent
DATA_DIR = TASK_DIR / "data"
MODEL_FILENAME = "model.pt"

# Official split sizes — used to sanity-check the vendored .npz.
SPLIT_SIZES = {"train": 13932, "val": 2452, "test": 8827}

# Frozen split subsample: read a deterministic 1/N slice of train and test so a
# full train + a 20-epoch base-vs-advanced comparison + the held-out scoring all
# finish quickly on CPU (this is the lever that keeps the run under the exec-env
# timeout). Both are 1/4 of the full official splits; val is ALWAYS scored in full
# (it drives model selection / early stopping). The test metric is therefore a
# deterministic 1/4 sample of the official test split — slightly less directly
# leaderboard-comparable than the full split, but the 0.70 bar is unchanged and
# the slice preserves class balance. (A CNN still clears the bar; the MLP still
# doesn't — both train on the same train slice, so the comparison stays fair.)
TRAIN_SUBSAMPLE = 4  # use every 4th train example (13932 -> 3483)
TEST_SUBSAMPLE = 4   # use every 4th test example  (8827 -> 2207)
SUBSAMPLE = {"train": TRAIN_SUBSAMPLE, "test": TEST_SUBSAMPLE}  # val: full (step 1)


# --- Data access (shared by training + eval; import these in your train script) -
def data_npz_path() -> Path:
    """Path to the vendored official ``.npz`` (all three splits in one file).

    Defaults to the task-local ``data/organsmnist.npz`` (checked in — the task
    runs fully offline with no download). Override with ``ORGANSMNIST_NPZ``.
    """
    return Path(os.environ.get("ORGANSMNIST_NPZ", DATA_DIR / f"{DATASET}.npz"))


def _require_data() -> Path:
    npz = data_npz_path()
    if not npz.is_file():
        raise FileNotFoundError(
            f"{npz} not found. The official splits are vendored in data/ — restore "
            f"them from git (or set ORGANSMNIST_NPZ). See prepare_data.py."
        )
    return npz


def load_split(split: str) -> tuple[np.ndarray, np.ndarray]:
    """Read one split straight from the vendored ``.npz``.

    Returns ``(images, labels)`` where ``images`` is uint8 ``[N, 28, 28]`` in the
    dataset's fixed order and ``labels`` is int64 ``[N]`` (0..10; see ``LABELS``).
    """
    if split not in SPLIT_SIZES:
        raise ValueError(f"unknown split {split!r}; expected train/val/test")
    npz = _require_data()
    with np.load(npz) as d:
        images = d[f"{split}_images"]
        labels = d[f"{split}_labels"].reshape(-1).astype(np.int64)
    step = SUBSAMPLE.get(split, 1)
    if step > 1:
        images = images[::step]   # deterministic 1/N slice (speed); val stays full
        labels = labels[::step]
    return np.ascontiguousarray(images), labels


def preprocess(images_uint8: np.ndarray) -> torch.Tensor:
    """uint8 ``[N, 28, 28]`` -> normalized float32 ``[N, 1, 28, 28]``.

    Exactly equivalent to torchvision ``ToTensor()`` (uint8 -> [0,1]) followed by
    ``Normalize(0.5, 0.5)`` (-> roughly [-1, 1]). Train and eval MUST use this.
    """
    x = torch.from_numpy(np.ascontiguousarray(images_uint8)).to(torch.float32).div_(255.0)
    x = x.unsqueeze(1)  # [N, 28, 28] -> [N, 1, 28, 28]
    return (x - NORM_MEAN) / NORM_STD


def load_dataset(split: str) -> torch.utils.data.TensorDataset:
    """Return a ``TensorDataset`` of ``(image[1,28,28] float, label int64)`` for ``split``.

    Items are in the dataset's fixed order, so predictions line up with the gold
    labels and the metric is comparable across runs. The whole split is small, so
    it is loaded into memory once.
    """
    images, labels = load_split(split)
    return torch.utils.data.TensorDataset(preprocess(images), torch.from_numpy(labels))


def pick_device(device: str | None = None) -> torch.device:
    """Resolve the compute device (honors CUDA_VISIBLE_DEVICES); cuda if available."""
    if device:
        return torch.device(device)
    return torch.device("cuda" if torch.cuda.is_available() else "cpu")


def load_model(model_path: str | os.PathLike) -> torch.jit.ScriptModule:
    """Load the agent-supplied TorchScript model with a helpful error on misuse."""
    p = Path(model_path)
    if not p.is_file():
        raise FileNotFoundError(
            f"model not found at {p}. Train one first: `python model.py` "
            f"(writes {MODEL_FILENAME})."
        )
    try:
        return torch.jit.load(str(p), map_location="cpu")
    except RuntimeError as exc:
        raise RuntimeError(
            f"{p} is not a loadable TorchScript model. Save your trained model with "
            f"`torch.jit.save(torch.jit.script(model.eval()), '{MODEL_FILENAME}')` "
            f"(or torch.jit.trace). Original error: {exc}"
        ) from exc


def save_torchscript(model: torch.nn.Module, path: str | os.PathLike) -> None:
    """Serialize a trained model to the TorchScript ``model.pt`` artifact.

    This is the **write** half of the model-artifact contract (``load_model`` is
    the read half). It lives here, frozen, so that *every* training recipe — the
    baseline's and yours — produces a byte-compatible artifact the scorer can
    load. Your ``train()`` in ``model.py`` calls this; you do not reimplement it.

    Tries ``torch.jit.script`` (handles control flow / any batch size); falls back
    to ``torch.jit.trace``. Saved on CPU for portability — the scorer moves it to
    the device itself.
    """
    model = model.eval().cpu()
    try:
        ts = torch.jit.script(model)
    except Exception as exc:  # noqa: BLE001 - trace is the robust fallback
        print(f"  (torch.jit.script failed: {type(exc).__name__}; falling back to trace)")
        example = torch.randn(2, N_CHANNELS, IMG_SIZE, IMG_SIZE)
        ts = torch.jit.trace(model, example)
    torch.jit.save(ts, str(path))


# --- Inference -----------------------------------------------------------------
@torch.no_grad()
def _predict(
    model: torch.nn.Module, split: str, device: torch.device, batch_size: int
) -> tuple[np.ndarray, np.ndarray]:
    """Run the model over a split (in official order); return (scores[N,C], labels[N])."""
    from torch.utils.data import DataLoader

    ds = load_dataset(split)
    loader = DataLoader(ds, batch_size=batch_size, shuffle=False, num_workers=0)
    model.eval().to(device)

    scores, labels = [], []
    for x, y in loader:
        logits = model(x.to(device, non_blocking=True).float())
        if logits.ndim != 2 or logits.shape[1] != N_CLASSES:
            raise ValueError(
                f"model output must be logits of shape [N, {N_CLASSES}]; got "
                f"{tuple(logits.shape)}. See the model contract in eval.py's docstring."
            )
        scores.append(torch.softmax(logits, dim=1).cpu().numpy())
        labels.append(np.asarray(y).reshape(-1))
    return np.concatenate(scores), np.concatenate(labels).astype(int)


def _per_class(y_true: np.ndarray, y_pred: np.ndarray) -> dict[str, Any]:
    out: dict[str, Any] = {}
    for c in range(N_CLASSES):
        support = int((y_true == c).sum())
        tp = int(((y_pred == c) & (y_true == c)).sum())
        pred_c = int((y_pred == c).sum())
        out[LABELS.get(c, str(c))] = {
            "support": support,
            "recall": round(tp / support, 4) if support else None,
            "precision": round(tp / pred_c, 4) if pred_c else None,
        }
    return out


def _score(y_true: np.ndarray, scores: np.ndarray) -> tuple[float, float]:
    """(auc, accuracy) — the MedMNIST leaderboard protocol, computed locally.

    Top-1 accuracy (argmax) + macro one-vs-rest AUC over the 11 classes, matching
    ``medmnist.Evaluator`` exactly without depending on that package.
    """
    from sklearn.metrics import accuracy_score, roc_auc_score

    y_pred = scores.argmax(axis=1)
    acc = float(accuracy_score(y_true, y_pred))
    auc = float(roc_auc_score(y_true, scores, multi_class="ovr", average="macro"))
    return auc, acc


# --- Public entry point --------------------------------------------------------
def evaluate(
    model_path: str | os.PathLike = MODEL_FILENAME,
    *,
    split: str = "val",
    batch_size: int = 256,
    device: str | None = None,
) -> dict[str, Any]:
    """Score a trained TorchScript model on a fixed split.

    With ``split`` in {"val", "train"} the result includes per-item detail
    (``samples`` + ``per_class`` + ``confusion``) for iteration. With
    ``split="test"`` the result is **aggregate-only** — no per-item log — so the
    individual held-out items can't be inspected. Use ``submit.py`` for the
    held-out test measurement rather than calling this with ``split="test"``.

    Primary metric is **accuracy** (the task bar is 0.70 on the test split);
    **AUC** (macro one-vs-rest) is reported alongside.
    """
    if split not in SPLIT_SIZES:
        raise ValueError(f"unknown split {split!r}; expected train/val/test")

    dev = pick_device(device)
    model = load_model(model_path)
    scores, y_true = _predict(model, split, dev, batch_size)

    auc, acc = _score(y_true, scores)
    y_pred = scores.argmax(axis=1)
    n = int(len(y_true))
    n_correct = int((y_pred == y_true).sum())

    result: dict[str, Any] = {
        "task": DATASET,
        "task_type": TASK_TYPE,
        "split": split,
        "dataset": f"MedMNIST OrganSMNIST ({IMG_SIZE}px) — official {split} split",
        "metric": "accuracy",
        "model_path": str(Path(model_path).resolve()),
        "device": str(dev),
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "n": n,
        "n_correct": n_correct,
        "auc": round(float(auc), 4),
        "accuracy": round(float(acc), 4),
    }

    # Per-item / per-class detail is overfitting fuel on held-out data, so it is
    # returned for train/val only. test stays aggregate-only on purpose.
    if split != "test":
        result["per_class"] = _per_class(y_true, y_pred)
        recalls = [d["recall"] for d in result["per_class"].values() if d["recall"] is not None]
        result["macro_recall"] = round(sum(recalls) / len(recalls), 4) if recalls else None
        # confusion[i][j] = # of class-i items predicted as class j
        cm = np.zeros((N_CLASSES, N_CLASSES), dtype=int)
        for t, p in zip(y_true, y_pred):
            cm[t, p] += 1
        result["confusion"] = cm.tolist()
        result["samples"] = [
            {
                "index": int(i),
                "true": int(y_true[i]),
                "true_label": LABELS.get(int(y_true[i])),
                "pred": int(y_pred[i]),
                "pred_label": LABELS.get(int(y_pred[i])),
                "p_pred": round(float(scores[i, y_pred[i]]), 4),
                "p_true": round(float(scores[i, y_true[i]]), 4),
                "is_correct": bool(y_pred[i] == y_true[i]),
            }
            for i in range(n)
        ]
    return result


def main() -> None:
    parser = argparse.ArgumentParser(description="Score model.pt on the val split.")
    parser.add_argument("--model", default=MODEL_FILENAME, help="Path to the TorchScript model.pt.")
    parser.add_argument(
        "--split", default="val", choices=["val", "train"],
        help="Iteration split. 'test' is reserved for submit.py.",
    )
    parser.add_argument("--batch-size", type=int, default=256)
    parser.add_argument("--device", default=None, help="e.g. cuda, cuda:0, cpu (default: auto).")
    parser.add_argument("--out", help="Write the full result JSON to this path.")
    args = parser.parse_args()

    result = evaluate(args.model, split=args.split, batch_size=args.batch_size, device=args.device)

    # Human-readable summary to stdout.
    print(f"\nsplit:       {result['split']}  (n={result['n']})")
    print(f"device:      {result['device']}")
    print(f"accuracy:    {result['accuracy']:.4f}   <- primary metric   ({result['n_correct']}/{result['n']})")
    print(f"AUC:         {result['auc']:.4f}   (macro one-vs-rest)")
    print(f"macro recall:{result['macro_recall']}")
    print("per-class:")
    for name, d in result["per_class"].items():
        print(f"  {name:<13} support={d['support']:<5} recall={d['recall']} precision={d['precision']}")
    n_wrong = result["n"] - result["n_correct"]
    print(f"misclassified: {n_wrong}")

    out_path = Path(args.out) if args.out else (TASK_DIR / "last_result.json")
    out_path.write_text(json.dumps(result, indent=2, ensure_ascii=False), encoding="utf-8")
    print(f"\nfull log written to {out_path}")


if __name__ == "__main__":
    main()
