"""Fixed evaluation harness for the `medical_reasoning` research task.

Contract
--------
You supply a *system prompt* (the thing being prompt-engineered). This harness
runs it against the fixed MedXpertQA samples, scores multiple-choice accuracy,
and returns a log.

There are two disjoint, seeded splits (see ``prepare_data.py``):

- ``train.jsonl`` — iterate here. ``evaluate(prompt)`` returns a *rich* per-sample
  log (reasoning, prediction vs. gold, per-question correctness) so you can see
  exactly what to fix.
- ``test.jsonl``  — report here, exactly once, via ``submit.py``. The test result
  is *aggregate-only*: no per-sample reasoning or gold labels are returned, so the
  held-out set can't be reverse-engineered or tuned against.

    from eval import evaluate
    result = evaluate("You are an expert physician. Think step by step...")
    print(result["accuracy"])        # e.g. 0.75   (train split by default)
    print(result["samples"][0])      # per-question detail (train only)

Or from the command line (train iteration):

    python eval.py --prompt-file my_prompt.txt
    python eval.py --prompt "You are a careful diagnostician..."

For the final, one-shot held-out number, use the separate submitter:

    python submit.py --prompt-file my_prompt.txt

DO NOT EDIT this file when iterating — only your prompt changes. Keeping the
harness fixed is what makes scores comparable across prompt revisions. The
model, the samples, the answer-extraction logic, and the scoring are all frozen
here on purpose.

Requires: ``pip install anthropic`` and ``ANTHROPIC_API_KEY`` in the environment.
"""

from __future__ import annotations

import argparse
import hashlib
import json
import os
import sys
import time
from concurrent.futures import ThreadPoolExecutor
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

import anthropic

# --- Frozen evaluation settings ------------------------------------------------
# The model is fixed so that accuracy differences reflect the *prompt*, not the
# model. Do not change these to tune a score.
FIXED_MODEL = "claude-opus-4-8"
MAX_TOKENS = 16000
EFFORT = "high"          # output_config effort for adaptive thinking
DEFAULT_WORKERS = 4      # parallel requests; does not affect scoring

TASK_DIR = Path(__file__).resolve().parent
SPLIT_PATHS = {
    "train": TASK_DIR / "train.jsonl",
    "test": TASK_DIR / "test.jsonl",
}

# --- Dashboard logging (side-effect only; never affects scoring) ---------------
# Every evaluate() call records one row + a status update and rebuilds the live
# dashboard, so the agent just calls evaluate() — no separate logging step. All of
# this is best-effort: a logging hiccup must never break or change an evaluation.
RESULTS_DIR = TASK_DIR / "results"
LEDGER_PATH = RESULTS_DIR / "leaderboard.jsonl"
STATUS_PATH = RESULTS_DIR / "status.json"
DASHBOARD_PATH = RESULTS_DIR / "dashboard.html"
_OPENED_SENTINEL = RESULTS_DIR / ".dashboard_opened"


def _prompt_id(prompt: str) -> str:
    """Stable short id so identical prompts share a name on the dashboard."""
    return "p-" + hashlib.sha256(prompt.encode("utf-8")).hexdigest()[:8]


def _read_ledger() -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    if LEDGER_PATH.is_file():
        for line in LEDGER_PATH.read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if line:
                try:
                    rows.append(json.loads(line))
                except Exception:
                    pass
    return rows


def _refresh_dashboard() -> None:
    try:
        from make_dashboard import build  # sibling module; rebuilds dashboard.html
        build()
    except Exception:
        pass


def _maybe_open_dashboard() -> None:
    """Open dashboard.html in a browser the first time the ledger is populated.

    Fires once (guarded by a sentinel). Works on a host with a desktop browser;
    inside a headless sandbox there is nothing to open, so it silently no-ops.
    Disable with ``DASHBOARD_AUTO_OPEN=0``.
    """
    if os.environ.get("DASHBOARD_AUTO_OPEN", "1") == "0":
        return
    if _OPENED_SENTINEL.exists():
        return
    try:
        _OPENED_SENTINEL.write_text("", encoding="utf-8")  # mark first, never retry-spam
        import webbrowser
        webbrowser.open(DASHBOARD_PATH.as_uri())
    except Exception:
        pass


def _write_status(**fields: Any) -> None:
    try:
        RESULTS_DIR.mkdir(exist_ok=True)
        fields.setdefault("ts", datetime.now(timezone.utc).isoformat())
        STATUS_PATH.write_text(json.dumps(fields), encoding="utf-8")
    except Exception:
        pass


def _log_result(result: dict[str, Any], prompt: str) -> None:
    """Append this run to the leaderboard and refresh the dashboard.

    Iteration is auto-derived: dev (train) runs count up; a test run is pinned to
    the current dev iteration. The dashboard groups by the labels "dev"/"test",
    so the train split is recorded as "dev".
    """
    try:
        rows = _read_ledger()
        first_row = not rows  # ledger was empty -> this is the first populated run
        split_label = "dev" if result.get("split") == "train" else result.get("split", "dev")
        if split_label == "dev":
            iteration = sum(1 for r in rows if r.get("split") == "dev")
        else:  # align a test checkpoint to the latest dev iteration on the chart
            iteration = max((r.get("iteration", 0) for r in rows), default=0)
        row = {
            "iteration": iteration,
            "prompt_name": _prompt_id(prompt),
            "split": split_label,
            "accuracy": result["accuracy"],
            "n_correct": result["n_correct"],
            "n": result["n"],
            "n_errors": result.get("n_errors", 0),
            "trigger": split_label,
            "ts": result.get("timestamp") or datetime.now(timezone.utc).isoformat(),
        }
        RESULTS_DIR.mkdir(exist_ok=True)
        with LEDGER_PATH.open("a", encoding="utf-8") as f:
            f.write(json.dumps(row) + "\n")
        dev_accs = [r["accuracy"] for r in rows if r.get("split") == "dev"]
        if split_label == "dev":
            dev_accs.append(row["accuracy"])
        _write_status(running=False, phase="idle", iteration=iteration,
                      best_dev=max(dev_accs) if dev_accs else None)
        _refresh_dashboard()
        if first_row:
            _maybe_open_dashboard()
    except Exception:
        pass


# --- Data ----------------------------------------------------------------------
def load_samples(split: str = "train") -> list[dict[str, Any]]:
    """Load a fixed, seeded MedXpertQA split (``"train"`` or ``"test"``)."""
    path = SPLIT_PATHS.get(split)
    if path is None:
        raise ValueError(
            f"unknown split {split!r}; expected one of {sorted(SPLIT_PATHS)}"
        )
    if not path.is_file():
        raise FileNotFoundError(
            f"{path} not found. Run `python prepare_data.py` to (re)build the splits."
        )
    samples = []
    with path.open(encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                samples.append(json.loads(line))
    return samples


def _format_question(sample: dict[str, Any]) -> str:
    """Render a sample as a plain multiple-choice question.

    MedXpertQA questions already embed their lettered answer choices inline, so
    we only append the options block when it is missing (e.g. a regenerated
    subsample from a source that stores choices separately).
    """
    question = sample["question"].strip()
    if "Answer Choices" in question:
        return question
    lines = [question, "", "Answer Choices:"]
    for letter, text in sorted(sample["options"].items()):
        lines.append(f"({letter}) {text}")
    return "\n".join(lines)


# --- Single-sample scoring -----------------------------------------------------
def _grade_one(
    client: anthropic.Anthropic,
    system_prompt: str,
    sample: dict[str, Any],
) -> dict[str, Any]:
    """Ask the fixed model one question and grade it.

    Answer extraction is made robust via structured outputs: the model must
    return JSON ``{"reasoning": ..., "answer": <one option letter>}``. The
    ``answer`` enum is restricted to this question's valid option letters, so
    the prediction is always a clean, scoreable letter — the prompt being
    engineered only has to shape the *reasoning*, not the output format.
    """
    letters = sorted(sample["options"].keys())
    schema = {
        "type": "object",
        "properties": {
            "reasoning": {
                "type": "string",
                "description": "Concise clinical reasoning toward the answer.",
            },
            "answer": {
                "type": "string",
                "enum": letters,
                "description": "The single best option letter.",
            },
        },
        "required": ["reasoning", "answer"],
        "additionalProperties": False,
    }

    record: dict[str, Any] = {
        "id": sample["id"],
        "medical_task": sample.get("medical_task"),
        "body_system": sample.get("body_system"),
        "question_type": sample.get("question_type"),
        "correct": sample["label"],
        "predicted": None,
        "is_correct": False,
        "reasoning": None,
        "latency_s": None,
        "usage": None,
        "error": None,
    }

    start = time.monotonic()
    try:
        response = client.messages.create(
            model=FIXED_MODEL,
            max_tokens=MAX_TOKENS,
            system=system_prompt,
            thinking={"type": "adaptive"},
            output_config={
                "effort": EFFORT,
                "format": {"type": "json_schema", "schema": schema},
            },
            messages=[{"role": "user", "content": _format_question(sample)}],
        )
        record["latency_s"] = round(time.monotonic() - start, 2)

        if response.stop_reason == "refusal":
            record["error"] = "refusal"
            return record

        text = next((b.text for b in response.content if b.type == "text"), "")
        parsed = json.loads(text)
        predicted = str(parsed.get("answer", "")).strip().upper()
        record["predicted"] = predicted
        record["reasoning"] = parsed.get("reasoning")
        record["is_correct"] = predicted == sample["label"]
        record["usage"] = {
            "input_tokens": response.usage.input_tokens,
            "output_tokens": response.usage.output_tokens,
        }
    except Exception as exc:  # noqa: BLE001 - log every failure, never abort the run
        record["latency_s"] = round(time.monotonic() - start, 2)
        record["error"] = f"{type(exc).__name__}: {exc}"

    return record


# --- Public entry point --------------------------------------------------------
def evaluate(
    prompt: str,
    *,
    split: str = "train",
    workers: int = DEFAULT_WORKERS,
    client: anthropic.Anthropic | None = None,
    log: bool = True,
) -> dict[str, Any]:
    """Score a candidate system prompt on a fixed split.

    With ``split="train"`` (default) the result includes a ``samples`` list with
    the detailed per-question log (reasoning, prediction, gold) for iteration.

    With ``split="test"`` the result is **aggregate-only**: overall ``accuracy``,
    counts, and the per-``medical_task`` breakdown, but **no per-sample log** —
    the held-out reasoning and gold labels are never returned, so the test set
    can't be inspected or tuned against. Use ``submit.py`` for the one-shot
    held-out measurement rather than calling this directly.

    Unless ``log=False``, the run is recorded to ``results/`` and the live
    dashboard is refreshed automatically — the caller does nothing extra.
    """
    if not prompt or not prompt.strip():
        raise ValueError("prompt must be a non-empty string")

    if log:
        _write_status(running=True, prompt=_prompt_id(prompt),
                      phase=f"scoring {'dev' if split == 'train' else split}")
        _refresh_dashboard()

    client = client or anthropic.Anthropic()
    samples = load_samples(split)

    if workers <= 1:
        results = [_grade_one(client, prompt, s) for s in samples]
    else:
        with ThreadPoolExecutor(max_workers=workers) as pool:
            results = list(pool.map(lambda s: _grade_one(client, prompt, s), samples))

    n = len(results)
    n_correct = sum(1 for r in results if r["is_correct"])
    n_errors = sum(1 for r in results if r["error"])

    by_task: dict[str, dict[str, int]] = {}
    for r in results:
        bucket = by_task.setdefault(r["medical_task"] or "Unknown", {"correct": 0, "total": 0})
        bucket["total"] += 1
        bucket["correct"] += int(r["is_correct"])

    result: dict[str, Any] = {
        "task": "medical_reasoning",
        "split": split,
        "dataset": f"MedXpertQA (Text) — fixed seed=42 {split} subset of {n}",
        "model": FIXED_MODEL,
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "n": n,
        "n_correct": n_correct,
        "n_errors": n_errors,
        "accuracy": round(n_correct / n, 4) if n else 0.0,
        "accuracy_by_medical_task": {
            k: round(v["correct"] / v["total"], 4) for k, v in sorted(by_task.items())
        },
        "prompt": prompt,
    }
    # The per-sample log (reasoning + gold) is overfitting fuel on held-out data,
    # so it is returned for train only. test stays aggregate-only on purpose.
    if split == "train":
        result["samples"] = results

    if log:
        _log_result(result, prompt)
    return result


def _read_prompt(args: argparse.Namespace) -> str:
    if args.prompt_file:
        return Path(args.prompt_file).read_text(encoding="utf-8")
    if args.prompt:
        return args.prompt
    if not sys.stdin.isatty():
        return sys.stdin.read()
    raise SystemExit("Provide a prompt via --prompt, --prompt-file, or stdin.")


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--prompt", help="System prompt string to evaluate.")
    parser.add_argument("--prompt-file", help="File containing the system prompt.")
    parser.add_argument("--workers", type=int, default=DEFAULT_WORKERS,
                        help="Parallel requests (does not affect scoring).")
    parser.add_argument("--out", help="Write the full result JSON to this path.")
    args = parser.parse_args()

    if not os.environ.get("ANTHROPIC_API_KEY"):
        raise SystemExit("Set ANTHROPIC_API_KEY in your environment first.")

    # eval.py is the *iteration* harness: train split only. The held-out test
    # number is produced once via submit.py.
    prompt = _read_prompt(args)
    result = evaluate(prompt, split="train", workers=args.workers)

    # Human-readable summary to stdout.
    print(f"\nsplit:    {result['split']}")
    print(f"model:    {result['model']}")
    print(f"accuracy: {result['accuracy']:.3f}  ({result['n_correct']}/{result['n']})")
    if result["n_errors"]:
        print(f"errors:   {result['n_errors']}")
    print("by medical_task:")
    for task, acc in result["accuracy_by_medical_task"].items():
        print(f"  {task:<24} {acc:.3f}")
    print("\nper-sample:")
    for r in result["samples"]:
        mark = "✓" if r["is_correct"] else ("!" if r["error"] else "✗")
        detail = r["error"] or f"pred={r['predicted']} gold={r['correct']}"
        print(f"  {mark} {r['id']:<12} {detail}")

    out_path = Path(args.out) if args.out else (TASK_DIR / "last_result.json")
    out_path.write_text(json.dumps(result, indent=2, ensure_ascii=False), encoding="utf-8")
    print(f"\nfull log written to {out_path}")


if __name__ == "__main__":
    main()
